// Programa que converte galões em litros.

package aprendizado.java;

public class GalToLit {

	public static void main(String[] args) {
	
		double galoes;
		double litros;
		
		galoes = 10; //começa com 10 galões
		
		litros = galoes * 3.7854; //converte para litros
		
		System.out.println(galoes + " Galões são " + litros + " litros.");

	}

}
